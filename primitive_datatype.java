
class primitive_datatype {

    public static void main(String args[]) {
        // System.out.print use for same line 
        System.out.print("Vaishu");
        System.out.println("Vaishu");
        System.out.println("Vaishu");
        System.out.println("Kesherwani");

        // PRIMITIVE DATATYPE 
        //1. INTEGER
        int a = 8;
        int b = 7;
        System.out.println(a + b);

        byte y = 28;
        System.out.println(y);

        short z = 31128;
        System.out.println(z);

        long x1 = 112845567;
        System.out.println(x1);

        // 2.FLOAT 
        float x = 7;
        System.out.println(x);
        float x2 = 7.7f;
        System.out.println(x2);

        double x3 = 79.098765467f;
        System.out.println(x3);

        // 3.CHARACTER
        char a1 = '9';
        System.out.println(a1);

        // 4.BOOLEAN
        boolean a2 = true;
        System.out.println(a2);
        boolean a3 = false;
        System.out.println(a3);

        // integer (whole number)
        int myNum = 5;
        System.out.println(myNum);
        // floating point number        
        float myFloatNum = 5.99f;
        System.out.println(myFloatNum);
        // character
        char myLetter = 'D';
        System.out.println(myLetter);
        // boolean 
        boolean myBool = true;
        System.out.println(myBool);
        // String 
        String myText = "Hello";
        System.out.println(myText);

        double m = 4.56461236555516;
        float n = 4.56461236555516f;

        System.out.println("double Accepts: " + m);
        System.out.println("Float Accepts: " + n);
    }
}
